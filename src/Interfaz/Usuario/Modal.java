    ArrayList<String> listaCategorias = new ArrayList();

    void setDatos() {
        AVL temp = EDDProyectoFinal.arbolAVL.raiz;
        categoriasRecursivo(temp);
        cbxSeleccion.setModel(new DefaultComboBoxModel<String>(listaCategorias.toArray(new String[0])));
    }

    void categoriasRecursivo(AVL nodo) {
        if (nodo == null) {
            return;
        }
        listaCategorias.add(nodo.categoria.categoria);

        if (nodo.izquierda != null) {
            categoriasRecursivo(nodo.izquierda);
        }
        if (nodo.derecha != null) {
            categoriasRecursivo(nodo.derecha);
        }
    }

    void generarReporte(String contenido) {
        Graphviz reporte = new Graphviz("salida.dot", "salida.png", contenido);
    }

