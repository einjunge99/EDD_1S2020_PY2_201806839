    private void jLabel40MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel40MouseClicked
        boolean bandera = true;
        long ISBN = 0;
        try {
            ISBN = Long.parseLong(txtISBN.getText());
        } catch (NumberFormatException e) {
            bandera = false;
        }
        if (bandera) {
            Libro temp = EDDProyectoFinal.arbolAVL.buscarLibro(ISBN);
            if (temp == null) {
                int usuario = Login.usuarioActual.carnet;
                String anio = txtAnio.getText();
                String autor = txtAutor.getText();
                String categoria = (String) cbxCategorias.getSelectedItem();
                String edicion = txtEdicion.getText();
                String titulo = txtTitulo.getText();
                String editorial = txtEditorial.getText();
                String idioma = txtIdioma.getText();
                Libro libro = new Libro(ISBN, titulo, autor, editorial, anio, edicion, categoria, idioma, usuario);
                EDDProyectoFinal.arbolAVL.buscar(categoria).categoria.libros.insertarLibro(libro);
                EDDProyectoFinal.contenidoBloque.crearLibro(libro);
                setDatos(true);
                JOptionPane.showMessageDialog(null, "Registro exitoso!");

            } else {
                JOptionPane.showMessageDialog(null, "Registro ISBN existente!");
            }

        }


    }//GEN-LAST:event_jLabel40MouseClicked

