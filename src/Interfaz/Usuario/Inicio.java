    void limpiarCategorias() {
        DefaultTableModel a = (DefaultTableModel) tablaCategoria.getModel();
        a.setRowCount(0);

    }

    void limpiarLibros() {

        DefaultTableModel b = (DefaultTableModel) tablaLibros.getModel();
        b.setRowCount(0);

    }

    void setDatos(boolean bandera) {
        listaCategorias.clear();
        limpiarCategorias();
        if (bandera) {
            limpiarLibros();
        }

        AVL temp = EDDProyectoFinal.arbolAVL.raiz;

        categoriasRecursivo(temp, bandera);
        llenarCategorias();

    }

    void llenarCategorias() {
        cbxCategorias.setModel(new DefaultComboBoxModel<String>(listaCategorias.toArray(new String[0])));
        int filas = listaCategorias.size();
        String[][] info = new String[filas][3];
        for (int i = 0; i < filas; i++) {
            info[i][0] = Integer.toString(i);
            info[i][1] = listaCategorias.get(i);
        }
        DefaultTableModel model = (DefaultTableModel) tablaCategoria.getModel();

        for (String[] fila : info) {
            model.addRow(fila);
        }

    }

    void categoriasRecursivo(AVL nodo, boolean bandera) {
        if (nodo == null) {
            return;
        }
        listaCategorias.add(nodo.categoria.categoria);
        if (bandera) {
            llenarLibros(nodo.categoria.libros.ingresados);
        }

        if (nodo.izquierda != null) {
            categoriasRecursivo(nodo.izquierda, bandera);
        }
        if (nodo.derecha != null) {
            categoriasRecursivo(nodo.derecha, bandera);
        }
    }

    void llenarLibros(ArrayList<Libro> arreglo) {
        int filas = arreglo.size();
        String[][] info = new String[filas][3];
        for (int i = 0; i < filas; i++) {
            info[i][0] = Long.toString(arreglo.get(i).ISBN);
            info[i][1] = arreglo.get(i).titulo;
            info[i][2] = arreglo.get(i).categoria;
        }
        DefaultTableModel model = (DefaultTableModel) tablaLibros.getModel();

        for (String[] fila : info) {
            model.addRow(fila);
        }

    }

    void setLibroActual(long ISBN, String categoria) {
        Libro temp = EDDProyectoFinal.arbolAVL.buscar(categoria).categoria.libros.buscar(ISBN);
        lblAnio.setText(temp.anio);
        lblAutor.setText(temp.autor);
        lblCategoria2.setText(categoria);
        lblISBN.setText(Long.toString(ISBN));
        lblEdicion.setText(temp.edicion);
        lblTitulo.setText(temp.titulo);
        lblEditorial.setText(temp.editorial);
        lblIdioma.setText(temp.idioma);

    }

